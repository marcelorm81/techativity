// ── Component ────────────────────────────────────────────────────────

export default function TechativityLogo({
  color = '#526426',
  width = '100%',
  className = '',
}: TechativityLogoProps) {
  const nodeAnims = useMemo(() => makeNodeAnims(NODES.length, 42), []);

  return (
    <div className={className} style={{ width, lineHeight: 0 }}>
      <svg
        xmlns="http://www.w3.org/2000/svg"
        viewBox="0 0 1658 583"
        fill="none"
        style={{ width: '100%', height: 'auto', display: 'block' }}
      >
        {NODES.map((node, i) => {
          const a = nodeAnims[i];
          return (
            <motion.g
              key={i}
              animate={{
                x: a.x,
                y: a.y,
                rotate: a.rotate,
              }}
              transition={{
                duration: a.duration,
                repeat: Infinity,
                ease: 'easeInOut',
                delay: a.delay,
              }}
              style={{ transformOrigin: 'center center' }}
            >
              {/* Filled shape */}
              <path
                d={node.d}
                fill={color}
                fillRule={node.fillRule}
                clipRule={node.clipRule}
              />
              {/* Stroke outline */}
              <path
                d={node.d}
                stroke={color}
                strokeWidth={2}
                strokeLinecap="round"
                fill="none"
                fillRule={node.fillRule}
                clipRule={node.clipRule}
              />
            </motion.g>
          );
        })}
      </svg>
    </div>
  );
}
