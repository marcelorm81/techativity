// ── Per-node animation parameters ────────────────────────────────────

interface NodeAnim {
  x: number[];
  y: number[];
  rotate: number[];
  duration: number;
  delay: number;
}

function makeNodeAnims(count: number, seed: number): NodeAnim[] {
  const anims: NodeAnim[] = [];
  for (let i = 0; i < count; i++) {
    const s = (seed + i * 137) % 1000;
    const dx = 0.8 + (s % 7) * 0.3;        // 0.8–2.6 px
    const dy = 0.6 + ((s * 3) % 9) * 0.25;  // 0.6–2.85 px
    const dr = 0.15 + ((s * 7) % 5) * 0.1;  // 0.15–0.65°
    const dur = 5 + (s % 11) * 0.5;          // 5–10.5s
    const del = (s % 13) * 0.3;              // 0–3.9s stagger

    anims.push({
      x: [0, dx, -dx * 0.6, dx * 0.3, 0],
      y: [0, -dy, dy * 0.4, -dy * 0.7, 0],
      rotate: [0, dr, -dr * 0.5, dr * 0.3, 0],
      duration: dur,
      delay: del,
    });
  }
  return anims;
}

