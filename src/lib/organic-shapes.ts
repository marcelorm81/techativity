// Shape keys for cycling / random access
export const SHAPE_KEYS = Object.keys(SHAPES) as (keyof typeof SHAPES)[];

/** Get a shape by index (wraps around) */
export function getShape(index: number): OrganicShape {
  return SHAPES[SHAPE_KEYS[index % SHAPE_KEYS.length]];
}

/** Get a random shape using a seed */
export function getSeededShape(seed: number): OrganicShape {
  return getShape(Math.abs(seed) % SHAPE_KEYS.length);
}

/** Pick N shapes for decorative use (evenly spaced through the set) */
export function pickShapes(count: number, offset = 0): OrganicShape[] {
  const result: OrganicShape[] = [];
  for (let i = 0; i < count; i++) {
    result.push(getShape(i + offset));
  }
  return result;
}

